public class Coordinate {

    private int x;
    private int y;

    public Coordinate(int x, int y){
        this.x  = x;
        this.y  = y;
    }


    /* ------- Getter & Setter ------- */

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
